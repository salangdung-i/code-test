const { solution } = require("../lv1/040801");

describe("동영상 재생기", () => {
  test("next, prev 명령어 기본 동작", () => {
    expect(solution("34:33", "13:00", "00:55", "02:55", ["next", "prev"])).toBe(
      "13:00",
    );
  });

  test("오프닝 구간 내에서 시작하면 오프닝 끝으로 이동", () => {
    expect(
      solution("10:55", "00:05", "00:15", "06:55", ["prev", "next", "next"]),
    ).toBe("06:55");
  });

  test("next 후 오프닝 구간 진입시 오프닝 끝으로 스킵", () => {
    expect(solution("07:22", "04:05", "00:15", "04:07", ["next"])).toBe(
      "04:17",
    );
  });

  // 엣지케이스도 추가해보면 좋아요
  test("prev로 0초 이하로 내려가면 00:00", () => {
    expect(solution("10:00", "00:05", "00:15", "02:00", ["prev"])).toBe(
      "00:00",
    );
  });

  test("next로 영상 끝 초과하면 영상 끝", () => {
    expect(solution("10:00", "09:55", "00:15", "02:00", ["next"])).toBe(
      "10:00",
    );
  });

  test("오프닝 시작점 정확히 도달하면 끝으로 스킵", () => {
    // prev로 딱 op_start에 떨어지는 경우
    expect(solution("10:00", "00:25", "00:15", "02:00", ["prev"])).toBe(
      "02:00",
    );
  });
});
